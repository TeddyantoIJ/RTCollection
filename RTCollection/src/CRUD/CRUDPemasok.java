    private void addData() {
        model.getDataVector().removeAllElements();

        model.fireTableDataChanged();

        model = controllerTable.addDataPemasok(model);

    }
    private void clear() {
        txtAlamat.setText("");
       
        txtHp.setText("");
        txtNama.setText("");
    }
