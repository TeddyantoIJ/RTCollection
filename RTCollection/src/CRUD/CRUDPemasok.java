    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (txtAlamat.getText().equals("") || txtHp.getText().equals("") || txtNama.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi data dengan benar!");
            return;
        }

  

        if (pemasok.getPms_id().equals("")) {
            pemasok.setPms_id(controlpemasok.getLastID());
        }

        pemasok.setPms_nama(txtNama.getText());
        pemasok.setPms_no_hp(txtHp.getText());
        pemasok.setPms_alamat(txtAlamat.getText());
        pemasok.setPms_jumlah_transaksi(0);
        pemasok.setPms_total_hutang(0);
        pemasok.setPms_uang_transaksi(0);

        if(controlpemasok.simpanPemasok(pemasok)){
            JOptionPane.showMessageDialog(this, "Berhasil menambah ...");
        }else{
            JOptionPane.showMessageDialog(this, "Gagal menambah ...");
        }

        addData();
    }//GEN-LAST:event_btnSimpanActionPerformed
    private void addColumn() {
        model.addColumn("No");
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("No HP");
        model.addColumn("Transaksi");
        model.addColumn("Pembayaran");
        model.addColumn("Hutang");
    }
    private void btnPerbaruiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPerbaruiActionPerformed
        // TODO add your handling code here:
        if (txtAlamat.getText().equals("") || txtHp.getText().equals("") || txtNama.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi data dengan benar!");
            return;
        }
        pemasok.setPms_nama(txtNama.getText());
        pemasok.setPms_no_hp(txtHp.getText());
        pemasok.setPms_alamat(txtAlamat.getText());
        
        try{
            controlpemasok.updatePemasok(pemasok);
            JOptionPane.showMessageDialog(this, "Berhasil memperbarui ...");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Gagal ...");
        }

        clear();
        addData();
    }//GEN-LAST:event_btnPerbaruiActionPerformed

